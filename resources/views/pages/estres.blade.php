                                Si es posible, el lugar donde se duerme debe ser tranquilo, oscuro y relajante, y se debe mantener a una temperatura agradable.
                                Limitar el uso de aparatos electrónicos como el televisor, las computadoras y los teléfonos inteligentes antes de dormir.
                                No comer en exceso ni tomar bebidas con alcohol o con cafeína antes de la hora de acostarse.
                                Hacer ejercicio físico. La actividad física durante el día puede ayudar a conciliar el sueño con más facilidad por la noche.
                                Manténgase en contacto con los demás. <br>
                                
                                Hable con sus familiares y amigos y explique sus preocupaciones y sus sentimientos a personas de confianza. Mantenerse en contacto con los demás le puede ayudar a sentirse más animado y menos estresado. <br>
                                
                                Lleve una dieta saludable <br>
                                
                                Todo lo que comemos y bebemos afecta a nuestra salud. Trate de alimentarse de forma equilibrada y de comer a intervalos regulares. Hidrátese bebiendo suficientemente y, si puede, tome abundantes frutas y hortalizas frescas. <br>
                                
                                Haga ejercicio con regularidad <br>
                                
                                Hacer ejercicio a diario le puede ayudar a reducir el estrés. Puede tratarse de ejercicios intensos o simplemente de caminar. <br>
                                
