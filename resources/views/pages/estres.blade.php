                                Siga una rutina diaria <br>
                                
                                Realizar una serie de actividades cada día nos puede ayudar a sentir que controlamos mejor nuestra vida y a ser más eficaces. Así, podemos programar momentos concretos para comer, pasar tiempo con nuestros familiares, hacer las tareas cotidianas y practicar ejercicio físico u otras actividades de ocio. <br>
                                
                                Duerma mucho <br>
                                
                                Es importante dormir lo suficiente para cuidar el cuerpo y la mente. El sueño repara, relaja y revitaliza nuestro organismo y nos ayuda a hacer frente a los efectos del estrés. <br>
                                
                                La higiene del sueño o, dicha de otro modo, los buenos hábitos de sueño, consisten en: <br>
                                
